const LanguageContext = createContext(null);

export function LanguageProvider({ children }) {
  const [lang, setLangState] = useState(() => {
    try {
      return localStorage.getItem('koberko_lang') || 'fil';
    } catch {
      return 'fil';
    }
  });

  const setLang = useCallback((newLang) => {
    setLangState(newLang);
    try {
      localStorage.setItem('koberko_lang', newLang);
    } catch {
      // localStorage unavailable — fail silently
    }
  }, []);

  /**
   * t(key) — returns the localized string for the current language.
   * If the key is missing, returns the key itself as a fallback (visible bug signal).
   */
  const t = useCallback(
    (key, values) => {
      const entry = strings[key];
      if (!entry) {
        console.warn(`[LanguageContext] Missing string key: "${key}"`);
        return key;
      }
      const template = entry[lang] ?? entry['fil'] ?? key;

      if (!values) {
        return template;
      }

      return template.replace(/\{(\w+)\}/g, (match, token) => {
        if (token in values) {
          return values[token];
        }

        return match;
      });
    },
    [lang],
  );

  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error('useLanguage must be used within a LanguageProvider');
  return ctx;
}

export default LanguageContext;
